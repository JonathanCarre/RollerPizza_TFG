package com.example.rollerpizza_tfg;

public class ReadWriteUserDetails {


    public String Nombre, Email, Telefono, Fecha, Password;


    public ReadWriteUserDetails(String Nombre, String Email, String Telefono, String Fecha, String Password) {
        this.Nombre = Nombre;
        this.Email = Email;
        this.Telefono = Telefono;
        this.Fecha = Fecha;
        this.Password = Password;
    }

}
